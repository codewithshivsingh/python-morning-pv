# type casting?
# type casting is about converting one type of data into another type.

# converting data into float using float()
print(float(598))
print(float(256.015))
# print(float(2+6j)) # complex -> float: not allowed
print(float(True))
print(float(False))
print(float("47.55")) # str -> float is possible only if str contains either int or float


# converting data into int using int()
print(int(48.251))
print(int(545))
# print(int(25+33j)) # complex -> int: not allowed
print(int(True))
print(int(False))
print(int("115")) # str -> int is possible only if str contains int


# str
print(str(2345))
print(str(2344.21))



